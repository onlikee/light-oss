export function useI18n() {
  const {
    preferences: { locale },
  } = useAppPreferences();

  return {
    locale,
    t: (key: TranslationKey, values?: Record<string, string | number>) =>
      translate(locale, key, values),
  };
}

export function translate(
  locale: AppLocale,
  key: TranslationKey,
  values?: Record<string, string | number>,
) {
  const template = dictionaries[locale][key] ?? enUS[key];

  if (!values) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_match, token) => {
    return String(values[token] ?? "");
  });
}

export function translateRecycleBinFailureReason(
  locale: AppLocale,
  code: string,
  message: string,
) {
  const byCode = recycleBinFailureReasonsByCode[code];
  if (byCode) {
    return byCode[locale];
  }

  const byMessage =
    recycleBinFailureReasonsByMessage[normalizeMessage(message)];
  if (byMessage) {
    return byMessage[locale];
  }

  return message;
}

function normalizeMessage(message: string) {
  return message.trim().toLowerCase();
}
